# a pizza with attributes
pizza = {
    "name": "Pepperoni",
    "size": "Extra Large",
    "toppings": ["Pepperoni", "Mozzarella", "Oregano", "Black olives", "Bacon"],
    "price": 9.99,
    "vegetarian": False,
}

print(pizza["name"])
# output --> Pepperoni
print(pizza["size"])
# output --> Extra Large
print(pizza["toppings"])
# output --> ['Pepperoni', 'Mozzarella', 'Oregano', 'Black olives', 'Bacon']
print(pizza["price"])
# output --> 9.99
print(pizza["vegetarian"])
# output --> False