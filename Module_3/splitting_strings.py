# splitting a string by a delimiter
# split the string at the comma and return a list of the split strings
text = "Hello, World! Welcome to Python!"
split_text = text.split(", ")
print(split_text)
# output --> ['Hello', 'World! Welcome to Python!']

# splitting a string into a list of words
sentence = "Python is a strong, powerful language."
words = sentence.split()
print(words)
# output --> ['Python', 'is', 'a', 'strong,', 'powerful', 'language.']