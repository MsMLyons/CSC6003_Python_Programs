# use ord() to change a character into a decimal value (ASCII)
print(ord('A'))
# output --> 65
print(ord('h'))
# output --> 104

# use chr() to change a decimal value (ASCII) into a character
print(chr(80))
# output --> P
print(chr(173))
# output --> ยก (extended ascii code - did not print to console, but left a space)